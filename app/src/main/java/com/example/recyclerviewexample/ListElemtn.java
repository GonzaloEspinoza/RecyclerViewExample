package com.example.recyclerviewexample;

public class ListElemtn {
    public String color;
    public String name;


    public ListElemtn(String color, String name) {
        this.color = color;
        this.name = name;

    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
